void main() {
  int angka1 = 10;
  int angka2 = 5;

  print('Hasil penjumlahan angka 1 + angka 2 = ${angka1 + angka2}');
  print('Hasil pengurangan angka 1 - angka 2 = ${angka1 - angka2}');
  print('Hasil perkalian angka 1 * angka 2 = ${angka1 * angka2}');
  print('Hasil pembagian angka 1 / angka 2 = ${angka1 / angka2}');
  print('Hasil sisa bagi angka 1 % angka 2 = ${angka1 % angka2}');
  print('Hasil pangkat angka 1 ^ angka 2 = ${angka1 ^ angka2}');
  print(
      'hasil bagi angka 1 dan angka 2 dengan tipe bilangan bulat = ${angka1 ~/ angka2}');
}
